definition(
    name: "Virtual Thermostat Creator",
    namespace: "baldeagle072",
    author: "Eric Roberts",
    description: "Create a virtual thermostat device",
    category: "Green Living",
    parent: "baldeagle072:Virtual Thermostat Manager",
    iconUrl: "https://s3.amazonaws.com/smartapp-icons/Convenience/Cat-Convenience.png",
    iconX2Url: "https://s3.amazonaws.com/smartapp-icons/Convenience/Cat-Convenience@2x.png",
    iconX3Url: "https://s3.amazonaws.com/smartapp-icons/Convenience/Cat-Convenience@2x.png")


preferences {
	section("Virtual Thermostat") {
		input "vTstatName", "text", title: "Thermostat Name", required: true
	}
}

def installed() {
	log.debug "Installed with settings: ${settings}"

	initialize()
}

def updated() {
	log.debug "Updated with settings: ${settings}"

	unsubscribe()
	initialize()
}

def initialize() {
    def deviceId = app.id + "vTstat"
    log.debug(deviceId)
    def existing = getChildDevice(deviceId)

	if (!existing) {
	    def childDevice = addChildDevice("baldeagle072", "Virtual Thermostat", deviceId, null, [label: vTstatName])           
	}
}

def uninstalled() {
    removeChildDevices(getChildDevices())
}

private removeChildDevices(delete) {
    delete.each {
        deleteChildDevice(it.deviceNetworkId)
    }
}
